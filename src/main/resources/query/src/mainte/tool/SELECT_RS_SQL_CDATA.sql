SELECT
   HEAD_REVISION
  ,PROJECT_NAME
  ,FILE_NAME
  ,PATH
  ,PERSISTER_NAME
  ,LINE
  ,TEXT
  ,REVISION
  ,AUTHOR
  ,COMMIT_YMD
  ,COMMIT_HMS
  ,EXTENSION
  ,TOROKUYMD
  ,TOROKUHMS
  ,TOROKUUSER
  ,KOSINYMD
  ,KOSINHMS
  ,KOSINUSER
  ,BIKO
FROM
  RS_SQL_CDATA
WHERE
  1 = 1
AND PATH = ?
AND PERSISTER_NAME = ?
AND TRIM(TEXT) IS NOT NULL
ORDER BY
    LINE DESC